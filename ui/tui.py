def print_dashboard():
    """Imprime el dashboard principal, movido a tui.py para reutilizaci√≥n."""
    # Usamos print_header para el dashboard
    print_header(
        "Dashboard del Sistema", current_context="Estado General", logo=logos.MAIN
    )
    width = get_terminal_width()

    print(f"{Colors.BOLD}Estado del Sistema:{Colors.RESET}")

    # Estado de dependencias
    try:
        subprocess.run(
            ["yt-dlp", "--version"],
            capture_output=True,
            check=True,
        )
        yt_dlp_status = f"{Colors.GREEN}Instalado{Colors.RESET}"
    except (FileNotFoundError, subprocess.CalledProcessError):
        yt_dlp_status = f"{Colors.RED}No Instalado{Colors.RESET}"

    try:
        subprocess.run(
            ["ffmpeg", "-version"],
            capture_output=True,
            check=True,
        )
        ffmpeg_status = f"{Colors.GREEN}Instalado{Colors.RESET}"
    except (FileNotFoundError, subprocess.CalledProcessError):
        ffmpeg_status = f"{Colors.RED}No Instalado{Colors.RESET}"

    print(f"  - yt-dlp: {yt_dlp_status}   |   ffmpeg: {ffmpeg_status}")
    print(
        f"  - Ruta de Descargas: "
        f"{Colors.YELLOW}{get_default_path()}{Colors.RESET}"
    )
    print_line("‚îÄ", width)


def display_main_menu(options, title="", current_context="", logo="", urlvideo_logo=""):
    """
    Muestra un men√∫ de opciones con un encabezado.

    Args:
        options (dict): Un diccionario donde las claves son las opciones (ej. "1", "s")
                        y los valores son la descripci√≥n de la opci√≥n.
        title (str): T√≠tulo principal del men√∫.
        current_context (str): Contexto actual del men√∫ (subt√≠tulo).
        logo (str): Logo a mostrar en el encabezado.
        urlvideo_logo (str): Logo espec√≠fico para la secci√≥n de video URL.
    """
    print_header(
        main_title=title,
        current_context=current_context,
        logo=logo,
        urlvideo_logo=urlvideo_logo,
    )
    width = get_terminal_width()

    for key, description in options.items():
        print_centered(f"{Colors.BOLD}{key}. {Colors.RESET}{description}", width)
    print_line("‚îÄ", width)


def get_menu_choice(valid_choices, prompt="Selecciona una opci√≥n: "):
    """
    Obtiene una opci√≥n v√°lida del usuario para un men√∫, mostrando un estilo de input.

    Args:
        valid_choices (list): Lista de opciones v√°lidas (ej. ["1", "2", "s"]).
        prompt (str): Mensaje a mostrar al usuario (ya no se usa directamente en el input).

    Returns:
        str: La opci√≥n v√°lida seleccionada por el usuario.
    """
    # Imprimir el estilo de input aleatorio, centrado
    input_style = get_random_input_style()
    print_centered(input_style)

    # El prompt ahora se maneja por el estilo, pero mantenemos una solicitud gen√©rica
    final_prompt = f"{Colors.BOLD}{Colors.GREEN}>> {Colors.RESET}"

    while True:
        choice = input(final_prompt).strip()
        if choice in valid_choices:
            return choice
        else:
            print(f"{Colors.RED}Opci√≥n no v√°lida. Int√©ntalo de nuevo.{Colors.RESET}")


