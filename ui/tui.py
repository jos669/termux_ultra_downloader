def strip_ansi(text):
    return re.sub(r'\x1b\[[0-9;]*m', '', text)


def get_terminal_width():
    try:
        return shutil.get_terminal_size().columns
    except OSError:
        return 80


def clear_screen():
    os.system("cls" if os.name == "nt" else "clear")


def print_line(char="‚îÄ", width=None):
    if width is None:
        width = get_terminal_width()
    print(char * width)


def confirm_action(prompt):
    # Importar aqu√≠ para evitar dependencia circular
    from utils.path_manager import get_user_input

    width = get_terminal_width()
    print_line("‚îÄ", width)
    response = get_user_input(
        f"{Colors.BOLD}{Colors.YELLOW}{prompt} (s/n): {Colors.RESET}"
    ).lower()
    print_line("‚îÄ", width)
    return response == "s"


def print_centered(text, width=80):
    print(text.center(width))


